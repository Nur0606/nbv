public class Gyl {
    private  String floverName;

    private double price;

    public Gyl(String floverName,  double price) {
        this.floverName = floverName;
        this.price = price;
    }

    public String getFloverName() {
        return floverName;
    }



    public double getPrice() {
        return price;
    }
}
